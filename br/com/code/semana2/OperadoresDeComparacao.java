package br.com.code.semana2;

public class OperadoresDeComparacao {
    public static void main(String[] args) {
        // maior que 
        System.out.println(10 > 5);

        // menor que
        System.out.println(10 < 5);

        // igualdade 
        System.out.println(10 == 5);

        // diferente

        System.out.println(10 != 5);

        // maior ou igual

        System.out.println(10 >= 10);

        System.out.println(10 <= 5 );
    }
}
